
package com.arfonfo.ej28_cinconaturales;


public class Ej28_CincoNaturales {

    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            if(i == 5) {
                System.out.print(i);
            } else {
                System.out.print(i + ", ");
            }
        }
    }
}
